
def added(value1, value2): 

    return value1+value2 
 

def multiply(value1, value2):    

    return value1 * value2 

   

def subtract(value1, value2): 

    return value1 - value2  

 

def divided(value1, value2): 

    return value1/value2 

   
def modular(value1, value2): 
    
    return value1 % value2 

    
   


